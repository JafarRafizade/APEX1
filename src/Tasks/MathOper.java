package Tasks;

public class MathOper {
    public int add (int a, int b){
        return a + b;

    }
    public int add (int a, double b){
        return (int) (a - b);

    }
    public double add (double a, double b){
        return a * b;

    }
    public int add (double a, int b){
        return (int) (a / b);

    }
}
